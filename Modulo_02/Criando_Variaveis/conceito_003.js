/*
Se a variável já foi declarada, basta recuperar o valor da variável através
do nome. Podemos fazer um teste imprimindo a variável com o comando
console.log.
*/

var serie = "Sherlock Holmes"
console.log(serie) //imprime "Sherlock Holmes"

/*
Além de recuperar, também podemos alterar o valor de uma variável. E
para realizar essa alteração, escreva o nome da variável seguido do sinal igual
(=).

Perceba que o comando var não precisa ser utilizado para atribuir o novo
valor a variável, somente na declaração.
*/

serie = "Os Mistérios de Miss Fisher"
console.log(serie) //imprime "Os Mistérios de Miss Fisher"